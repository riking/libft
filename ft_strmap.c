#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char			*r;
	unsigned int	i;

	r = ft_strdup(s);
	i = 0;
	while (s[i])
	{
		r[i] = f(s[i]);
		i++;
	}
	return (r);
}
